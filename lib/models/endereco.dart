
class Endereco {
  String rua;
  String cidade;
  String bairro;
  String estado;
  String cep;
  int numero;

  Endereco(rua, cidade, estado, bairro, cep, numero) {
    this.rua = rua;
    this.cidade = cidade;
    this.bairro = bairro;
    this.estado = estado;
    this.cep = cep;
    this.numero = numero;
  }
}